	/**
	 * Register the stylesheets for the admin area.
	 *
	 * @since    1.0.0
	 */
	public function enqueue_styles()
	{

		/**
		 * This function is provided for demonstration purposes only.
		 *
		 * An instance of this class should be passed to the run() function
		 * defined in Gbyc_Loader as all of the hooks are defined
		 * in that particular class.
		 *
		 * The Gbyc_Loader will then create the relationship
		 * between the defined hooks and the functions defined in this
		 * class.
		 */

		wp_enqueue_style($this->plugin_name, plugin_dir_url(__FILE__) . 'css/gbyc-admin.css', array(), $this->version, 'all');
	}

	/**
	 * Register the JavaScript for the admin area.
	 *
	 * @since    1.0.0
	 */
	public function enqueue_scripts()
	{

		/**
		 * This function is provided for demonstration purposes only.
		 *
		 * An instance of this class should be passed to the run() function
		 * defined in Gbyc_Loader as all of the hooks are defined
		 * in that particular class.
		 *
		 * The Gbyc_Loader will then create the relationship
		 * between the defined hooks and the functions defined in this
		 * class.
		 */

		wp_enqueue_script($this->plugin_name, plugin_dir_url(__FILE__) . 'js/gbyc-admin.js', array('jquery'), $this->version, false);
	}

	// add woocommerce new setting tab
	function gbyc_add_settings_tab($settings_tabs)
	{
		$settings_tabs['gbyc_settings_tabs'] = __('Gateway by Country', 'gbyc');
